//////////////////////////////////////////////////////////////////////////////////////////////////
// import dependencies
//////////////////////////////////////////////////////////////////////////////////////////////////
import dotenv from "dotenv";
import express from "express";
import readRouter from "./routes/readRoutes.mjs";

//////////////////////////////////////////////////////////////////////////////////////////////////
// load environment variables from .env, define express app and port number
//////////////////////////////////////////////////////////////////////////////////////////////////

dotenv.config();

const app = express();
const PORT = process.env.PORT;

//////////////////////////////////////////////////////////////////////////////////////////////////
// route handlers
//////////////////////////////////////////////////////////////////////////////////////////////////

app.use("/shipwrecks", readRouter);

//////////////////////////////////////////////////////////////////////////////////////////////////
// listen to port
//////////////////////////////////////////////////////////////////////////////////////////////////

app.listen(PORT, () => {
  console.log(`Server running on port ${PORT}`);
});
