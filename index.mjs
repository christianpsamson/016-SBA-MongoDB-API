//////////////////////////////////////////////////////////////////////////////////////////////////
// import dependencies
//////////////////////////////////////////////////////////////////////////////////////////////////
import dotenv from "dotenv";
import express from "express";
import connectDB from "./db/conn.mjs";

//////////////////////////////////////////////////////////////////////////////////////////////////
// load environment variables from .env, define express app and port number
//////////////////////////////////////////////////////////////////////////////////////////////////

dotenv.config();

const app = express();
const PORT = process.env.PORT;

//////////////////////////////////////////////////////////////////////////////////////////////////
// call connectDB function for testing
//////////////////////////////////////////////////////////////////////////////////////////////////

connectDB();

//////////////////////////////////////////////////////////////////////////////////////////////////
// listen to port
//////////////////////////////////////////////////////////////////////////////////////////////////

app.listen(PORT, () => {
  console.log(`Server running on port ${PORT}`);
});
